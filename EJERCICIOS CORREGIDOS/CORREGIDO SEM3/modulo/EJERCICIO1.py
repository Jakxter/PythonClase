def Split(texto):
    texto_en_split = texto.split()
    print(texto_en_split)

def Join(lista):
    texto_en_join = " ".join(lista)
    print(texto_en_join)

def Count(texto,word):
    texto_en_count = texto.count(word)
    print(texto_en_count)

def Find(texto,find):
    texto_en_find = texto.find(find)
    print(texto_en_find)

def Upper(texto):
    texto_en_uppercase = texto.upper()
    print(texto_en_uppercase)

def Lower(texto):
    texto_en_lowercase = texto.lower()
    print(texto_en_lowercase)

